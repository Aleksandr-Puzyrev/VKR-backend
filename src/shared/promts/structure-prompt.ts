export const structurePrompt = (topic: string) => {
  const prompt = `
    Ты — эксперт по созданию образовательных программ. Сначала сгенерируй подробную структуру курса в формате JSON на основе названия: "${topic}".
    Формат ответа:
      {
        "title": "Название курса",
        "description": "Краткое описание курса",
        "audience": "Для кого предназначен курс (новички, специалисты, студенты и т.д.)",
        "duration": "Ожидаемое время прохождения (например: 5 часов, 3 недели и т.п.)",
        "goals": "Цель 1, Цель 2, Цель 3",
        "modules": [
          {
            "title": "Название модуля",
            "description": "Описание модуля",
            "lessons": [
              {
                "title": "Название урока",
                "description": "Краткое описание, что будет в этом уроке"
              }
            ]
          }
        ]
      }
    Не нужно писать сам контент уроков — только структуру и план курса.
    Структура должна быть глубокой и профессиональной: минимум 5 модулей, в каждом — 3–6 уроков. Курс должен быть логически выстроен: от простого к сложному. Обязательно включи итоговый проект или дипломную часть в последнем модуле.
    Ответ — только в JSON, без пояснений.
    `;
  return prompt;
};
